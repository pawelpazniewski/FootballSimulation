package Resources;

public class Team {



    public String teamName;
    public int teamStrength;


    public Team (String teamName, int teamStrength){
        this.teamName = teamName;
        this.teamStrength = teamStrength;
    }
    public String getTeamName() {
        return teamName;
    }

    public int getTeamStrength() {
        return teamStrength;
    }


}
